#include "ofNoisyFire.h"

//--------------------------------------------------------------
void ofNoisyFire::setup() {

	ofSetFrameRate(60);
	ofSetWindowTitle("Voxel Cube - Noisy Fire");

	ofBackground(255);
	ofEnableDepthTest();

	this->frame.setMode(ofPrimitiveMode::OF_PRIMITIVE_LINES);
}

//--------------------------------------------------------------
void ofNoisyFire::update() {

	this->face.clear();
	this->frame.clear();

	int span = 15;
	int len = 300;
	for (int x = len * -0.5; x <= len * 0.5; x += span) {
		for (int y = len * -0.5; y <= len * 0.5; y += span) {
			for (int z = len * -0.5; z <= len * 0.5; z += span) {

				auto noise_value = ofNoise(x * 0.01, z * 0.01, y * 0.01 + ofGetFrameNum() * 0.03);

				if (y > len * 0.35) {
					noise_value += ofMap(y, ofGetHeight() * 0.35, len * 0.5, 0.25, 1);
				} else if (y < 0.15) {
					noise_value += ofMap(y, len * -0.5, len * 0.15, -0.65, 0);
				}

				if (noise_value > 0.35) {
					this->setBoxToMesh(this->face, this->frame, glm::vec3(x, y, z), span);
				}
			}
		}
	}
}

//--------------------------------------------------------------
void ofNoisyFire::draw() {

	this->cam.begin();
	ofRotateX(180);
	ofRotateY(ofGetFrameNum() * 0.5);

	ofSetHexColor(0xF73718);
	this->face.draw();

	ofSetColor(239);
	this->frame.drawWireframe();

	this->cam.end();
}

//--------------------------------------------------------------
void ofNoisyFire::setBoxToMesh(ofMesh& face_target, ofMesh& frame_target, glm::vec3 location, float size) {

	this->setBoxToMesh(face_target, frame_target, location, size, size, size);
}

